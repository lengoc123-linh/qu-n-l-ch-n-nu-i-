    private void btnEditAccount1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditAccount1ActionPerformed
        // TODO add your handling code here:
        if (tblFarm.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn farm cần chỉnh sửa!");
        } else {
            int check = JOptionPane.showConfirmDialog(this, "Bạn có muốn chỉnh sửa farm này?", "Chỉnh sửa", JOptionPane.YES_NO_OPTION);
            if (check == JOptionPane.YES_OPTION) {
                // Lấy thông tin farm cần chỉnh sửa từ bảng
                int row = tblFarm.getSelectedRow();
                int farmId = (Integer) tblFarm.getValueAt(row, 0);  // Giả sử farmId là cột đầu tiên trong bảng
                Farm farmToEdit = FarmDAO.getInstance().selectById(farmId);  // Lấy farm theo ID

                // Hiển thị các ô nhập liệu cho người dùng nhập thông tin mới
                String newFarmName = JOptionPane.showInputDialog(this, "Nhập tên farm mới:", farmToEdit.getFarmName());
                if (newFarmName == null || newFarmName.trim().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Tên farm không được để trống!");
                    return;
                }

                String newAddress = JOptionPane.showInputDialog(this, "Nhập địa chỉ farm mới:", farmToEdit.getAddress());
                if (newAddress == null || newAddress.trim().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Địa chỉ farm không được để trống!");
                    return;
                }

                String newOwner = JOptionPane.showInputDialog(this, "Nhập tên chủ sở hữu mới:", farmToEdit.getOwner());
                if (newOwner == null || newOwner.trim().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Chủ sở hữu không được để trống!");
                    return;
                }

                // Cập nhật farm
                farmToEdit.setFarmName(newFarmName);
                farmToEdit.setAddress(newAddress);
                farmToEdit.setOwner(newOwner);

                try {
                    // Cập nhật farm vào cơ sở dữ liệu
                    FarmDAO.getInstance().update(farmToEdit);
                    JOptionPane.showMessageDialog(this, "Cập nhật farm thành công!");
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Cập nhật farm không thành công!");
                }

                // Tải lại danh sách farm sau khi cập nhật
                ArrayList<Farm> farms = (ArrayList<Farm>) FarmDAO.getInstance().selectAll();
                loadDataToTable(farms);  // Hàm này sẽ làm mới bảng farm
            }
        }
    }//GEN-LAST:event_btnEditAccount1ActionPerformed

