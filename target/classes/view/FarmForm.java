    private void btnDeleteAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteAccountActionPerformed
            // Kiểm tra nếu không có farm nào được chọn
            // Kiểm tra nếu không có farm nào được chọn
            // Kiểm tra nếu không có farm nào được chọn
            if (tblFarm.getSelectedRow() == -1) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn farm cần xóa !");
            } else {
                
                // Lấy giá trị farmid từ dòng đã chọn
                int farmId = (int) tblFarm.getValueAt(tblFarm.getSelectedRow(), 0); // Cột farmid là cột đầu tiên

                // Xác nhận xóa farm
                int checkVl = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa farm này ?", "Xác nhận xóa farm", JOptionPane.YES_NO_OPTION);
                if (checkVl == JOptionPane.YES_OPTION) {
                    try {
                        // Gọi hàm xóa farm từ DAO
                        int result = FarmDAO.getInstance().delete(farmId);
                        if (result > 0) {
                            JOptionPane.showMessageDialog(this, "Xóa farm thành công!");
                            // Cập nhật lại bảng sau khi xóa
                            loadDataToTable((ArrayList<Farm>) FarmDAO.getInstance().selectAll());
                        } else {
                            JOptionPane.showMessageDialog(this, "Không thể xóa farm này!");
                        }
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(this, "Lỗi khi xóa farm: " + e.getMessage());
                    }
                }
            }

    }//GEN-LAST:event_btnDeleteAccountActionPerformed

    private void exportExcelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exportExcelActionPerformed
        // TODO add your handling code here:
        try {
            JFileChooser jFileChooser = new JFileChooser();
            jFileChooser.showSaveDialog(this);
            File saveFile = jFileChooser.getSelectedFile();
            if (saveFile != null) {
                saveFile = new File(saveFile.toString() + ".xlsx");
                Workbook wb = new XSSFWorkbook();
                Sheet sheet = wb.createSheet("Account");

                Row rowCol = sheet.createRow(0);
                for (int i = 0; i < tblFarm.getColumnCount(); i++) {
                    Cell cell = rowCol.createCell(i);
                    cell.setCellValue(tblFarm.getColumnName(i));
                }
                for (int j = 0; j < tblFarm.getRowCount(); j++) {
                    Row row = sheet.createRow(j + 1);
                    for (int k = 0; k < tblFarm.getColumnCount(); k++) {
                        Cell cell = row.createCell(k);
                        if (tblFarm.getValueAt(j, k) != null) {
                            cell.setCellValue(tblFarm.getValueAt(j, k).toString());
                        }
                    }
                }
                FileOutputStream out = new FileOutputStream(new File(saveFile.toString()));
                wb.write(out);
                wb.close();
                out.close();
                openFile(saveFile.toString());
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_exportExcelActionPerformed

