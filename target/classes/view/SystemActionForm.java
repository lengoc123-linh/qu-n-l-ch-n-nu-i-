    private void btnDeleteAccessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteAccessActionPerformed
        // Kiểm tra xem có dòng nào được chọn trong bảng không
    if (tblSystemAction.getSelectedRow() == -1) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn hành động hệ thống cần xoá !");
                } else {
                    // Lấy đối tượng hành động hệ thống được chọn
                     SystemActionHistory select = getSystemActionSelect();

                    // Xác nhận xóa từ người dùng
                    int checkVl = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa hành động hệ thống này ?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);

                    if (checkVl == JOptionPane.YES_OPTION) {
                        try {                        
                            // Thực hiện xóa hành động hệ thống
                            SystemActionHistoryDAO.getInstance().delete(select);

                            // Thông báo thành công và tải lại dữ liệu vào bảng
                            JOptionPane.showMessageDialog(this, "Xoá hành động hệ thống thành công !");
                            loadDataToTable(SystemActionHistoryDAO.getInstance().selectAll());  // Tải lại dữ liệu vào bảng
                        } catch (Exception e) {
                            // Thông báo lỗi nếu có vấn đề khi xóa
                            JOptionPane.showMessageDialog(this, "Xoá hành động hệ thống thất bại !");
                            e.printStackTrace();
                        }
                    }
                }
    }//GEN-LAST:event_btnDeleteAccessActionPerformed

    private void exportExcelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exportExcelActionPerformed
        // TODO add your handling code here:
        try {
            JFileChooser jFileChooser = new JFileChooser();
            jFileChooser.showSaveDialog(this);
            File saveFile = jFileChooser.getSelectedFile();
            if (saveFile != null) {
                saveFile = new File(saveFile.toString() + ".xlsx");
                Workbook wb = new XSSFWorkbook();
                Sheet sheet = wb.createSheet("Account");

                Row rowCol = sheet.createRow(0);
                for (int i = 0; i < tblSystemAction.getColumnCount(); i++) {
                    Cell cell = rowCol.createCell(i);
                    cell.setCellValue(tblSystemAction.getColumnName(i));
                }
                for (int j = 0; j < tblSystemAction.getRowCount(); j++) {
                    Row row = sheet.createRow(j + 1);
                    for (int k = 0; k < tblSystemAction.getColumnCount(); k++) {
                        Cell cell = row.createCell(k);
                        if (tblSystemAction.getValueAt(j, k) != null) {
                            cell.setCellValue(tblSystemAction.getValueAt(j, k).toString());
                        }
                    }
                }
                FileOutputStream out = new FileOutputStream(new File(saveFile.toString()));
                wb.write(out);
                wb.close();
                out.close();
                openFile(saveFile.toString());
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_exportExcelActionPerformed

