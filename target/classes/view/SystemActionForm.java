    private ArrayList<SystemActionHistory> searchSystemActionHistory(String luachon, String searchContent) {
        ArrayList<SystemActionHistory> result = new ArrayList<>();

        switch (luachon) {
            case "Tất cả":
                result = SearchSystemActionHistory.getInstance().searchTatCaSystemActionHistory(searchContent);
                break;
            case "Loại hành động":
                result = SearchSystemActionHistory.getInstance().searchByActionType(searchContent);
                break;
            case "Mô tả":
                result = SearchSystemActionHistory.getInstance().searchByDescription(searchContent);
                break;
            case "Người thực hiện":
                result = SearchSystemActionHistory.getInstance().searchByPerformedBy(searchContent);
                break;
            case "Thời gian hành động":
                result = SearchSystemActionHistory.getInstance().searchByActionTime(searchContent);
                break;
            default:
                JOptionPane.showMessageDialog(this, "Lựa chọn tìm kiếm không hợp lệ!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                break;
        }

        return result;
    }
